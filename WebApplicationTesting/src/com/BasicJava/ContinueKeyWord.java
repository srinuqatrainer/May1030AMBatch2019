package com.BasicJava;

public class ContinueKeyWord {

	public static void main(String[] args) {
		
		for(int a=1;a<=5;a++)
		{
			if(a == 3)
			{
				continue;
			}
			
			System.out.println(a+" "+"LiveTech");
		}

 // continue keyWord skips the current iteration and continues with the rest of iterations
	}

}
