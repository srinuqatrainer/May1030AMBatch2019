package com.BasicJava;

public class VariableExample {

	public static void main(String[] args) {
		
		// dataType variable=value;
		
		// a variable should be assigned with a value and should be declared with a dataType
		// a variable can save only one value at a time
		// a variable declared with a dataType cannot save other dataType values
// a variable once declared with a dataType need not be declared again when a new value		                                                                   is assigned to it
		
		int x=100;
		x=200;
		String y="hello";
		String z="300";
		double p=12.345;
		float k=15;
		
		char i='M';
		
		System.out.println(x);
		System.out.println(y);
		System.out.println(z);
		System.out.println(p);
		System.out.println(k);
		System.out.println(i);

	}

}
